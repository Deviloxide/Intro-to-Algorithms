def gcd(a, b):
    r = a % b

    while r != 0:
        a = b
        b = r
        r = a % b

    return b


print(gcd(12, 4))
print(gcd(640895567899799998756978640895567899799998756978640895567899799998756978,
          66408955678997999987569787867876867990056767))


def gcd_rec(a, b):
    if b == 0:
        return a
    else:
        return gcd_rec(b, a % b)


print(gcd(12, 4))
